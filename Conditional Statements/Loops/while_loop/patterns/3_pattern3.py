i = 1
while i <= 5:
    j = 1
    while j <= i:
        print("*",end= " ")
        j+=1
    print(" ")
    i+=1

k = 4 
while k >=1:
    l = 1
    while l<=k:
        print("*",end=" ")
        l +=1
    print(" ")
    k-=1
